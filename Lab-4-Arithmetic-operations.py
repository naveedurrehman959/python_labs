# arithmetic_operations.py
num1= int(input("enter the first number:"))
num2= int(input("enter the second number:"))

# addtition
additions_result= num1 + num2
print(f"addition: {num1} + {num2} = {additions_result}")
#subtraction
subtraction_result= num1 - num2
print(f"subtraction: {num1} - {num2}= {subtraction_result}")

# multiplication
multiplication_result= num1 * num2
print(f"multiplication: {num1} * {num2}= {multiplication_result}")

# Performing division
division_result = num1 / num2
print(f"Division: {num1} / {num2} = {division_result}")
# performing integer division
integer_division = num1 // num2
print(f"Integer Division: {num1} // {num2} = {integer_division}")

# Performing float division
float_division_result = num1 / num2
print(f"Float Division: {num1} / {num2} = {float_division_result}")
